#!/bin/sh

sed \
    -e '/^ *=[^=]/d' \
    -e 's@^[ ]*==\([^=]\)@=\1@' \
    -e 's@^[ ]*===\([^=]\)@==\1@' \
    -e 's@^[ ]*====\([^=]\)@===\1@' \
    -e 's@^[ ]*=====\([^=]\)@====\1@' \
    -e 's@^[ ]*======\([^=]\)@=====\1@' \
    -e 's@^\(=[^=][^=]*\)==\([^=]*\)$@\1=\2@' \
    -e 's@^\(==[^=][^=]*\)===\([^=]*\)$@\1==\2@' \
    -e 's@^\(===[^=][^=]*\)====\([^=]*\)$@\1===\2@' \
    -e 's@^\(====[^=][^=]*\)=====\([^=]*\)$@\1====\2@' \
    -e 's@^\(=====[^=][^=]*\)======\([^=]*\)$@\1=====\2@' \
    $1
